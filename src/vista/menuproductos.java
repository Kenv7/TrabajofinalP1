    private void listarProductos(){
        List<Productos> lista= frp.Listar();
        modeloProductos= (DefaultTableModel) tblproductos.getModel();
        Object[] ob=new Object[6];
        for(int i=0;i<lista.size();i++){
            ob[0]=lista.get(i).getIdProducto();
            ob[1]=lista.get(i).getNombreProducto();
            ob[2]=lista.get(i).getMarcaProducto();
            ob[3]=lista.get(i).getCategoriaProducto();
            ob[4]=lista.get(i).getPrecioProducto();
            ob[5]=lista.get(i).getStockProducto();
            modeloProductos.addRow(ob);
        }
        tblproductos.setModel(modeloProductos);
    
    }

